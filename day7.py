# Operators
# Arithmetic Operators
print(3 + 5)                   # addition operator (+)
print(3 - 5)                   # subtraction  operator (-)
print(3 * 5)                   # multiplication operator (*)
print(10 / 2)                  # division operator (/)
print(7 % 4)                   # modulus operator (%)
print(3 ** 5)                  # exponentiation operator (**)
print(9 // 4)                  # floor division operator (//)

